# or
print(0 | 0)  # 0
print(0 | 1)  # 1
print(1 | 0)  # 1
print(1 | 1)  # 1
# and
print(0 & 0)  # 0
print(0 & 1)  # 0
print(1 & 0)  # 0
print(1 & 1)  # 1
# xor
print(0 ^ 0)  # 0
print(0 ^ 1)  # 1
print(1 ^ 0)  # 1
print(1 ^ 1)  # 0
# not
print(0)  # 0
print(~0)  # -1
print(1)  # 1
print(~1)  # -2

print(bin(0))  # 0b0
print(bin(~0))  # -0b1
print(bin(1))  # 0b1
print(bin(~1))  # -0b10

# bit shift
print(bin(1 << 0))  # 0b1
print(bin(1 << 1))  # 0b10
print(bin(1 << 2))  # 0b100
print(bin(1 << 3))  # 0b1000

